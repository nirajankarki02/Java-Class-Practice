package day3;

public class RelationalOperator {
    public static void main(String[] args) {
        //Operators:

        //4. Relation Operator:
        // > < >= <= == !=
        int a = 4;
        int b = 5;

        System.out.println(a > b); //false
        System.out.println(a < b);//true
        System.out.println(a >= b);//false
        System.out.println(a <= b);//true
        System.out.println(a != b);//true
        System.out.println(a == b);//false


    }
}
