package day5;

public class If {
    public static void main(String[] args) {
        int a = 5;
        int b = 6;

        if (a > b)
        {
            System.out.println("a is greater than b.");
        }
        if (b > a)
        {
            System.out.println("b is greater than a.");
        }

        System.out.println("==================");
    }

}
